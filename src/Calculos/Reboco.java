    public void calcularSacoDeCimento() {
        volume = 25;
        double sacoCimento = 38;
        
        metros = Double.parseDouble(jtfMetros.getText().replaceAll(",", "."));
        espessuraArgmassa = Double.parseDouble(jtfArgamassa.getText().replaceAll(",", "."));
        
        double espessuraReboco = espessuraArgmassa / 100;
        
        double reboco = espessuraReboco * metros;
        
        double volumeLitros = (volume * reboco)/100;
        
        double litros = volumeLitros * 1000;
        
        double resultado = litros / sacoCimento;
        jtfResultado.setText(String.format("%.2f", resultado) + " Sacos de cimentos.");
        
        double latas = (reboco * 75);
        
        double resultado2 = (latas / 38.13) * 10;
        
        jtfResultado2.setText(String.format("%.2f", resultado2) + " Latas de Areia.");
    }
    
    

